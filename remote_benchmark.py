def download_fragment(host, port, filename, offset, length, worker_id):
    try:
        s = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
        s.settimeout(30.0)
        s.connect((host, port))
        
        req = f"GET {filename} {offset} {length}\n"
        s.sendall(req.encode('utf-8'))
        
        received = 0
        raw_data = bytearray()
        while True:
            chunk = s.recv(8192)
            if not chunk:
                break
            raw_data.extend(chunk)
            
        try:
            decompressed = gzip.decompress(raw_data)
            received = len(decompressed)
        except Exception:
            received = len(raw_data)
            
        s.close()
        return received
    except Exception as e:
        print(f"Worker {worker_id} error: {e}")
        return 0

def get_file_size(host, port, filename):
    s = socket.socket(socket.AF_INET, socket.SOCK_STREAM)
    s.settimeout(5.0)
    s.connect((host, port))
    s.sendall(f"SIZE {filename}\n".encode('utf-8'))
    size_str = s.recv(1024).decode('utf-8').strip()
    s.close()
    if size_str.startswith("ERROR"):
        raise Exception(size_str)
    return int(size_str)

def run_benchmark(host, port, filename, num_workers, target_mb=10):
    print(f"  ▶ {num_workers} daemon(s)...", end='', flush=True)
    try:
        file_size = get_file_size(host, port, filename)
    except Exception as e:
        print(f"Failed to get file size: {e}")
        return None

    chunk_size = 1024 * 1024
    fragments = []
    
    test_size = min(file_size, target_mb * 1024 * 1024)
    offset = 0
    while offset < test_size:
        length = min(chunk_size, test_size - offset)
        fragments.append((offset, length))
        offset += length
        
    start_time = time.time()
    
    lock = threading.Lock()
    total_downloaded = [0]
    
    def worker_thread(worker_id):
        while True:
            lock.acquire()
            if not fragments:
                lock.release()
                break
            frag = fragments.pop(0)
            lock.release()
            
            bytes_dl = download_fragment(host, port, filename, frag[0], frag[1], worker_id)
            
            lock.acquire()
            total_downloaded[0] += bytes_dl
            lock.release()

    threads = []
    for i in range(num_workers):
        t = threading.Thread(target=worker_thread, args=(i,))
        threads.append(t)
        t.start()
        
    for t in threads:
        t.join()
        
    duration_ms = int((time.time() - start_time) * 1000)
    total_mb = test_size / (1024 * 1024)
    speed = total_mb / (duration_ms / 1000.0) if duration_ms > 0 else 0
    
    print(f" {duration_ms:,} ms ({speed:.2f} MB/s)")
    return {"daemons": num_workers, "sizeMB": int(total_mb), "ms": duration_ms, "mbps": speed}

def generate_reports(results, delay=0):
    print("\n[4/4] Writing CSV...")
    base_ms = results[0]["ms"]
    
    # Calculate speedups
    for r in results:
        r["speedup"] = base_ms / r["ms"] if r["ms"] > 0 else 1.0

    # Write CSV
    with open("benchmark_results.csv", "w", encoding="utf-8") as f:
        f.write("daemons,mode,file_size_mb,duration_ms,speed_mbps,speedup\n")
        for r in results:
            mode = "sequential" if r["daemons"] == 1 else "parallel"
            f.write(f"{r['daemons']},{mode},{r['sizeMB']},{r['ms']},{r['mbps']:.2f},{r['speedup']:.2f}\n")
    print("  ✓ benchmark_results.csv")

    # Write HTML
    print("[5/5] Generating chart...")
    labels = [str(r["daemons"]) for r in results]
    times = [str(r["ms"]) for r in results]
    speeds = [f"{r['mbps']:.2f}" for r in results]
    speedups = [f"{r['speedup']:.2f}" for r in results]
    
    rows = ""
    for r in results:
        mode = "Sequential" if r["daemons"] == 1 else "Parallel"
        rows += f"<tr><td>{r['daemons']}</td><td>{mode}</td><td>{r['ms']:,}</td><td>{r['mbps']:.2f}</td><td>{r['speedup']:.2f}x</td></tr>"

